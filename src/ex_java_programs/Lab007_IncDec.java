package ex_java_programs;

public class Lab007_IncDec {
    public static void main(String[] args) {
        int a1=10, a2=10, a3=10, a4 = 10;

        System.out.println(a1++); //10
        System.out.println(a2--); //10
        System.out.println(++a3); //11
        System.out.println(--a4); //9
        System.out.println("----");
        System.out.println(a1); //11
        System.out.println(a2); //9
        System.out.println(a3); //11
        System.out.println(a4); //9

    }
}
